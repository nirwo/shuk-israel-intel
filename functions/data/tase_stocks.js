/**
 * All TASE stocks combined with index metadata
 */
function getAllStocks() {
  const addIndex = (stocks, index) => stocks.map(s => ({ ...s, index }));
  return [
    ...addIndex(TA35, 'TA-35'),
    ...addIndex(TA90, 'TA-90'),
    ...addIndex(TA_SME, 'SME'),
  ];
}

function getStocksByIndex(indexName) {
  const all = getAllStocks();
  if (indexName === 'all') return all;
  return all.filter(s => s.index === indexName);
}

